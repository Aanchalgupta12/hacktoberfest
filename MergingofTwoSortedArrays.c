#include<stdio.h>
int Merge_sort(int A[],int B[],int m,int n)
{
    int i=0;
    int j=0;
    int C[m+n];
    int k=0;
    int l;
    while(i<m && j<n)
    {
        if(A[i]<B[j])
        {
            C[k]=A[i];
            k=k+1;
            i=i+1;
        }
        else
        {
            C[k]=B[j];
            j=j+1;
            k=k+1;
        }
    }
    while(i<m)
    {
        C[k]=A[i];
        i=i+1;
        k=k+1;
    }
    while(j<m)
    {
        C[k]=B[j];
        j=j+1;
        k=k+1;
    }
    for(l=0;l<m+n;l++)
    {
        printf("%d\t",C[l]);
    }
}
/***************************************************************************/
void main()
{
    int a[5]={4,7,9,10,11};
    int b[5]={3,8,13,14,15};
    int m=5;
    int n=5;
    Merge_sort(a,b,m,n);
}
